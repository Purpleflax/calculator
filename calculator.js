function addNum(numOne, numTwo) {
    return(parseInt(numOne + numTwo));
}
function subtractNum(numOne, numTwo) {
    return(parseInt(numOne - numTwo));
}
function divideNum(numOne, numTwo) {
    return(parseInt(numOne / numTwo));
}
function multiplyNum(numOne, numTwo) {
    return(parseInt(numOne * numTwo));
}